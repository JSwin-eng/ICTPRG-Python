
#lists or arrays [] square brackets are needed for a list

this_list = ["apple","banana","cherry","pear"]
print(this_list)

#remove() removes item with specified value
this_list.remove("banana")
print(this_list)

#append() adds element at end of the list

this_list.append("orange")
print(this_list)

#pop() removes element at specified position
this_list.pop(0)
print(this_list) 

words = "Hello world I am here"
