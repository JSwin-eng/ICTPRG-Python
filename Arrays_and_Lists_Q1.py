# Given the following python code:

# values = [66, 43, 1, 6, 2, 99, 4]   

# Output each number on a separate line if it is less than the number 10.

values = [66, 43, 1, 6, 2, 99, 4]

x = 0

while x < len(values):
    if values[x] < 10:
        print(values[x])
    x += 1






